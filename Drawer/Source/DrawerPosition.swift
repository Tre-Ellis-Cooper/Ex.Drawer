import Foundation

/// An enum representing the possible drawer positions
/// with respect to the parent container.
enum DrawerPosition {
    /// Bottom-edge placement.
    case bottom
    /// Leading-edge placement.
    case leading
    /// Top-edge placement.
    case top
    /// Trailing-edge placement
    case trailing
}
